void spell_enchant_weapon( int sn, int level, CHAR_DATA *ch, void *vo )
{
    OBJ_DATA *obj = (OBJ_DATA *) vo;
    AFFECT_DATA *paf;
    AFFECT_DATA *paf_next;
    char log_buf[MAX_STRING_LENGTH];
    int hit = 0, dam = 0;

    if ( obj->item_type != ITEM_WEAPON )
	return;

    for ( paf = obj->affected; paf != NULL; paf = paf_next )
    { paf_next = paf->next;
      if ( paf->location == APPLY_HITROLL )
      { hit += paf->modifier;
      obj_affect_remove( obj, paf );
      }
      if ( paf->location == APPLY_DAMROLL )
      { dam += paf->modifier;
        obj_affect_remove( obj, paf );
      }
    }
    if ( affect_free == NULL )
    {
	paf		= alloc_perm( sizeof(*paf), PERM_AFF );
    }
    else
    {
	paf		= affect_free;
	affect_free	= affect_free->next;
    }

    paf->type		= sn;
    paf->duration	= -1;
    paf->location	= APPLY_HITROLL;
    paf->modifier	= hit + 1;
    paf->bitvector	= 0;
    paf->next		= obj->affected;
    obj->affected	= paf;

    if ( affect_free == NULL )
    {
	paf		= alloc_perm( sizeof(*paf), PERM_AFF );
    }
    else
    {
	paf		= affect_free;
	affect_free	= affect_free->next;
    }

    paf->type		= sn;
    paf->duration	= -1;
    paf->location	= APPLY_DAMROLL;
    paf->modifier	= dam + 1;
    paf->bitvector	= 0;
    paf->next		= obj->affected;
    obj->affected	= paf;
    obj->level		= number_fuzzy( ch->level - 5 );

    send_to_char( "Ok.\n\r", ch );
    sprintf( log_buf, "Enchant used by %s on %s (%d).", ch->name, obj->short_descr, obj->pIndexData->vnum );
    log_string( log_buf );
    return;
}



