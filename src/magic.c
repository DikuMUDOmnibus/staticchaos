void spell_dispel_magic( int sn, int level, CHAR_DATA *ch, void *vo )
{ AFFECT_DATA *paf;
  AFFECT_DATA *paf_next;
  CHAR_DATA *victim = (CHAR_DATA *) vo;
  int num = 0;
  int count = 0;
  int hr = skill_lookup( "holy resist" );

  if ( IS_NPC(victim) )
  { send_to_char( "Why bother?\n\r", ch );
    return;
  }
  for ( paf = victim->affected; paf != NULL; paf = paf_next )
  { paf_next = paf->next;
    num++;
  }
  if ( num < 1 )
  { send_to_char( "They have no spells to dispel.\n\r", ch );
    return;
  }

  act( "A silvery ray lances toward $N.", ch, NULL, victim, TO_CHAR );
  act( "A silvery ray lances toward you!", ch, NULL, victim, TO_VICT );
  act( "A silvery ray lances toward $N", ch, NULL, victim, TO_NOTVICT );

  num = dice( 1, num );

  /* If the target has holy resist up */
  if ( is_affected(victim,hr) )
  {
    for ( paf = victim->affected; paf->type != hr; paf = paf->next )
      ;

    paf->duration = UMAX(0,paf->duration - 300); 
    if ( paf->duration > 0 )
    {
      act( ".. but is absorbed by $N's barrier.", ch, NULL, victim, TO_CHAR );
      act( ".. but is absorbed by your barrier.", ch, NULL, victim, TO_VICT );
      act( ".. but is absorbed by $N's barrier.", ch, NULL, victim, TO_NOTVICT );
    }
    else
    {
      act( ".. and collapses $S barrier!", ch, NULL, victim, TO_CHAR );
      act( ".. and collapses your barrier!", ch, NULL, victim, TO_VICT );
      act( ".. and collapses $S barrier!", ch, NULL, victim, TO_NOTVICT );
    }
  }
  else  /* otherwise.. */
  {
    for ( paf = victim->affected; paf != NULL; paf = paf_next )
    { paf_next = paf->next;
      count++;
      if ( count == num )
      { affect_remove( victim, paf );
        if ( paf->type > 0 && skill_table[paf->type].msg_off )
          send_to_char( skill_table[paf->type].msg_off, victim );
          send_to_char( "\n\r", victim );
      }
    }
  }

}


