  obj->value[0] = vnum;
  obj->timer = 1;
  obj_to_room( obj, ch->in_room );
  act( "You create a portal to $N.", ch, NULL, victim, TO_CHAR );
  act( "A shimmering portal appears.\n\r", ch, NULL, victim, TO_ROOM );
  return;
