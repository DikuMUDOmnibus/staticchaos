  { send_to_char( "A portal begins to open, then is sucked into oblivion.\n\r", ch );
    return;
  }
  vnum = victim->in_room->vnum;
  if ( (vnum >= 8900 && vnum <= 8999)
    || (vnum >= 5300 && vnum <= 5399)
    || (vnum >= 8400 && vnum <= 8599)
    || (vnum >= 8100 && vnum <= 8299) )
    vnum = 5148;
